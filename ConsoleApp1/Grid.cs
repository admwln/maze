    public Grid()
    {
        SpawnCoins();
    }
    
    private void SpawnCoins()
    {
        // Spawn 20 coins randomly
        var rnd = new Random();
        var counter = 0;
        while (counter < 21 )
        {
            var row = rnd.Next(1, 21);
            var col = rnd.Next(1, 49);
            // If random coordinate is not a path, continue
            if (Arr[row, col] != 1) continue;
            // If there already is a coin in the neighboring area, continue
            if (CheckNeighborsForCoins(row, col)) continue;
            Arr[row, col] = 4;
            counter++;
        }
    }
    private bool CheckNeighborsForCoins(int row, int col)
    {
        if (Arr[row - 1, col] == 4) return true;
        if (Arr[row + 1, col] == 4) return true;
        if (Arr[row, col - 1] == 4) return true;
        return Arr[row, col + 1] == 4;
    }
